#include "libft.h"

void		*ft_calloc(size_t nmemb, size_t size)
{
	void	*str;

	str = malloc(size * nmemb);
	if (str != NULL)
	{
		ft_bzero(str, size * nmemb);
		return (str);
	}
	return (NULL);
}
